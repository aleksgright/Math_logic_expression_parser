public class Operations {

    static boolean _conjunction(boolean a, boolean b){
        return a&&b;
    }

    static boolean _disjunction(boolean a, boolean b){
        return a||b;
    }

    static boolean _denial(boolean a){
        return !a;
    }

    static boolean _implication(boolean a, boolean b){
        return !a||b;
    }
}

